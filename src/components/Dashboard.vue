<script>
import EventBus from "@/AppEventBus";
import Vue3autocounter from "vue3-autocounter";
import { decodePubkey } from "@cosmjs/proto-signing";
import { DirectSecp256k1HdWallet } from "@cosmjs/proto-signing";

export default {
  name: "Dashboard",
  components: {
    "vue3-autocounter": Vue3autocounter,
  },
  data() {
    return {
      blockHeightClass: "no-highlight",
      scaleClass: false,
			pubKey: null,
			loadValues: {},
    };
  },
  watch: {},


  created() {
		// Get the initial values
		fetch(process.env.VUE_APP_DCL_RPC_NODE + "/block").then(response => response.json()).then(data => {
			this.loadValues = data;
		});

    // Get all the models
    this.$store.dispatch(
      "zigbeealliance.distributedcomplianceledger.model/QueryModelAll",
      {
        options: {
          subscribe: true,
          all: true,
        },
      }
    );

    // Get all the accounts
    this.$store.dispatch(
      "zigbeealliance.distributedcomplianceledger.dclauth/QueryAccountAll",
      {
        options: {
          subscribe: true,
          all: true,
        },
      }
    );
    // Get all the pending accounts
    this.$store.dispatch(
      "zigbeealliance.distributedcomplianceledger.dclauth/QueryPendingAccountAll",
      {
        options: {
          subscribe: true,
          all: true,
        },
      }
    );
    // Get all the revoked accounts
    this.$store.dispatch(
      "zigbeealliance.distributedcomplianceledger.dclauth/QueryPendingAccountRevocationAll",
      {
        options: {
          subscribe: true,
          all: true,
        },
      }
    );

    // Get all the validators
    this.$store.dispatch(
      "zigbeealliance.distributedcomplianceledger.validator/QueryValidatorAll",
      {
        options: {
          subscribe: true,
          all: true,
        },
      }
    );


  },
  computed: {
		currentKey: {
			get() {
				return this.pubKey;
			},
			set(value) {
				this.pubKey = value;
			}
		},
    isSignedIn() {
      const loggedIn = this.$store.getters["common/wallet/loggedIn"];
			if(loggedIn) {
				this.updatePubKey();
			}
      return loggedIn;
    },
		currentAddress() {

			if(this.$store.getters["common/wallet/loggedIn"]) {
			const wallet = this.$store.getters['common/wallet/wallet']
      const accounts = wallet.accounts
      const account = wallet.accounts[0]
      return account.address
			}
			return '';
		},
    blockHeight: {
      get() {
        return this.blockHeightClass;
      },
      set(v) {
        this.blockHeightClass = v;
      },
    },

    modelCount() {
      const ModelInfoArray =
        this.$store.getters[
          "zigbeealliance.distributedcomplianceledger.model/getModelAll"
        ]();
      return ModelInfoArray?.model.length || 0;
    },

    validatorCount() {
      const ValidatorInfoArray =
        this.$store.getters[
          "zigbeealliance.distributedcomplianceledger.validator/getValidatorAll"
        ]();
      return ValidatorInfoArray?.validator?.length || 0;
    },

		jailedValidatorCount() {
			const ValidatorInfoArray =
				this.$store.getters[
					"zigbeealliance.distributedcomplianceledger.validator/getValidatorAll"
				]();
			return ValidatorInfoArray?.validator?.filter(e=>e.jailed).length || 0;
		},

    allProposedAccounts() {
      const pendingAccountsArray =
        this.$store.getters[
          "zigbeealliance.distributedcomplianceledger.dclauth/getPendingAccountAll"
        ]();
      return pendingAccountsArray?.pendingAccount || 0;
    },

    allActiveRevocations() {
      const pendingAccountRevocationArray =
        this.$store.getters[
          "zigbeealliance.distributedcomplianceledger.dclauth/getPendingAccountRevocationAll"
        ]();
      return pendingAccountRevocationArray?.pendingAccountRevocation || 0;
    },

    allActiveAccounts() {
      const allAccountsArray =
        this.$store.getters[
          "zigbeealliance.distributedcomplianceledger.dclauth/getAccountAll"
        ]();
      return allAccountsArray?.account || 0;
    },

    trusteeCount() {
      if (!this.allActiveAccounts) return 0;
      return this.allActiveAccounts.filter((account) =>
        account.roles.includes("Trustee")
      ).length;
    },
    vendorCount() {
      if (!this.allActiveAccounts) return 0;
      return this.allActiveAccounts.filter((account) =>
        account.roles.includes("Vendor")
      ).length;
    },
    nodeAdminCount() {
      if (!this.allActiveAccounts) return 0;
      return this.allActiveAccounts.filter((account) =>
        account.roles.includes("NodeAdmin")
      ).length;
    },
    certificationCenterCount() {
      if (!this.allActiveAccounts) return 0;
      return this.allActiveAccounts.filter((account) =>
        account.roles.includes("CertificationCenter")
      ).length;
    },
    currentBlockHeight() {
      const blockArray = this.$store.state.common.blocks.blocks;
      if (!blockArray || blockArray.length === 0)  {
        if(this.loadValues.result)
					return parseInt(this.loadValues.result.block.header.height);
      }
      const lastBlock = blockArray[blockArray.length - 1];
      this.scaleClass = true;
      if (lastBlock) return parseInt(lastBlock.height);
      else return 0;

    },

		lastBlockParticipatingNodes() {

			let blockArray = []
			if(this.loadValues.result) {
				blockArray = [this.loadValues.result.block]
			} else {
				return [];
			}


			if(this.$store.state.common.blocks.blocks && this.$store.state.common.blocks.blocks.length > 0) {
				blockArray = this.$store.state.common.blocks.blocks;
			}

      const lastBlock = blockArray[blockArray.length - 1];
			const details = lastBlock.details || lastBlock;

			if(details) {
				return details.last_commit.signatures.filter(e=>e.signature)
			} else {
				return [];
			}
		},


    previousBlockHeight() {
      const blockArray = this.$store.state.common.blocks.blocks;
      if (!blockArray || blockArray.length < 2) {
        return 0;
      }
      const lastBlock = blockArray[blockArray.length - 2];
      return parseInt(lastBlock.height);
    },
    currentUser() {},

  },
  methods: {
    theFormat(number) {
      return number.toFixed(0);
    },
    completed() {
      this.scaleClass = false;
    },
		shortenKey (key) {
			if(!key) return '';
			return key.substr(0, 4) + '...' + key.slice(-4)
		},
		shortenAddress(addr) {
			if(!addr) return '';
			return addr.substr(0, 10) + '...' + addr.slice(-5)
		},
		copyToClipboard(value) {
			const el = document.createElement('textarea');
			el.value = value;
			document.body.appendChild(el);
			el.select();
			document.execCommand('copy');
			document.body.removeChild(el);
		},
		updatePubKey() {
			if (this.$store.getters["common/wallet/loggedIn"]) {
      DirectSecp256k1HdWallet.fromMnemonic(
        this.$store.state["common"]["wallet"]["activeWallet"].mnemonic
      ).then((data) => {
        data.getAccountsWithPrivkeys().then((data) => {
          const defaultPubkeyBytes = data[0].pubkey;
          const defaultPubkeyProtoBytes = Uint8Array.from([
            0x0a,
            defaultPubkeyBytes.length,
            ...defaultPubkeyBytes,
          ]);
          const decodedPubKey = decodePubkey({
            typeUrl: "/cosmos.crypto.secp256k1.PubKey",
            value: defaultPubkeyProtoBytes,
          });
          this.currentKey = decodedPubKey;
        });
      });
    }
		}


  },
};
</script>

